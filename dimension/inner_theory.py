"""
ТЕОРИЯ НАШЕГО МИРА: строим поверх 7 законов.

Законы:
  1. Ткань = инъекция (16 → 8 позиций)
  2. Ёмкость позиции = ёмкость ячейки (1:1)
  3. НЕЛИНЕЙНО (суперпозиция не работает)
  4. Входы СИММЕТРИЧНЫ
  5. Выходы СИММЕТРИЧНЫ
  6. Расстояние НЕ сохраняется
  7. Инъективна на наблюдаемых масштабах

Из закона 1+2: 16 входных позиций → 8 выходных. Соотношение 2:1.
Значит: на каждую выходную ячейку приходится 2 входных.
"Избыток" = 16 - 8 = 8 позиций.

Из закона 7: инъективна на 50K. Но 16→8 = сжатие.
Значит: collision ДОЛЖНА существовать (pigeonhole в наших терминах:
  больше входов чем выходов → два входа делят один выход).

ПАРАДОКС: закон 7 (инъективна) vs закон 1 (сжатие 2:1).
Разрешение: collision существует, но на масштабе >> 50K.

ТЕОРЕМА 1 (НАТИВНАЯ): Collision СУЩЕСТВУЕТ.
Доказательство: 16 позиций входа, 8 позиций выхода.
  Ёмкость входа > ёмкость выхода (закон 2).
  → ∃ два различных входа с одинаковым выходом.

ВОПРОС: СКОЛЬКО следов нужно сгенерировать чтобы НАЙТИ collision?
Нативный ответ: зависит от СТРУКТУРЫ ткани, не от "бит".

Новый эксперимент: ищем СТРУКТУРНЫЕ свойства, предсказывающие collision.
"""

import numpy as np
import struct, hashlib

def sha256_words(W16):
    return struct.unpack('>8I', hashlib.sha256(struct.pack('>16I', *W16)).digest())

def hw(x): return bin(x).count('1')


def main():
    np.random.seed(42)

    print("=" * 70)
    print("ТЕОРИЯ: от законов к collision")
    print("=" * 70)

    # =================================================================
    print(f"\n{'=' * 70}")
    print("ТЕОРЕМА 1: Collision существует (пигеонхол в наших терминах)")
    print("=" * 70)

    # Ёмкость: сколько различных значений может принять одна позиция?
    # Эксперимент: замеряем для входа и выхода.

    N_capacity = 200000
    W_base = [np.random.randint(0, 2**32) for _ in range(16)]

    # Input capacity (one position)
    in_cap = len(set(np.random.randint(0, 2**32, N_capacity)))
    print(f"\n  Ёмкость 1 входной позиции (из {N_capacity}): {in_cap}")

    # Output capacity (one position)
    out_values = set()
    for _ in range(N_capacity):
        W = list(W_base)
        W[0] = np.random.randint(0, 2**32)
        H = sha256_words(W)
        out_values.add(H[0])

    out_cap = len(out_values)
    print(f"  Ёмкость 1 выходной позиции: {out_cap}")
    print(f"  Ratio: {in_cap / max(out_cap, 1):.4f}")

    # Ёмкость ПОЛНОГО входа vs ПОЛНОГО выхода
    # 16 позиций × C_in vs 8 позиций × C_out
    # Если C_in ≈ C_out: пространство входа = C^16, выхода = C^8.
    # Ratio = C^16 / C^8 = C^8.
    # → C^8 следов в среднем делят один выход.

    C = out_cap  # ёмкость одной позиции ≈ in_cap ≈ out_cap

    print(f"\n  Ёмкость C ≈ {C}")
    print(f"  Входное пространство: C^16 = {C}^16")
    print(f"  Выходное пространство: C^8 = {C}^8")
    print(f"  Избыток: C^16 / C^8 = C^8 = {C}^8")
    print(f"  → Каждый выход разделяют в среднем C^8 входов")

    # =================================================================
    print(f"\n{'=' * 70}")
    print("ТЕОРЕМА 2: Масштаб collision = √(C^8) = C^4")
    print("=" * 70)

    # В нашем мире: overlay (⊕) создаёт метки из пар следов.
    # N следов → N(N-1)/2 overlays.
    # Каждый overlay: P(метка=0) = 1/C^8 (выходное пространство).
    # N²/2 × 1/C^8 ≥ 1 → N ≥ √(2×C^8) = √2 × C^4

    # В НАШИХ единицах: масштаб collision = C^4.

    print(f"\n  Overlay (⊕): N следов → N² overlays")
    print(f"  P(overlay=0) = 1/(выходное пространство) = 1/C^8")
    print(f"  N² × 1/C^8 ≥ 1 → N ≥ C^4")
    print(f"\n  Масштаб collision = C^4")
    print(f"  В наших единицах: {C}^4 = {C**4:.2e}")

    # =================================================================
    print(f"\n{'=' * 70}")
    print("ТЕОРЕМА 3: Ёмкость C — ЧТО ЭТО?")
    print("=" * 70)

    # C ≈ 200000 (из нашего эксперимента). Но это НИЖНЯЯ граница!
    # Мы генерировали 200K значений. Все оказались различны.
    # → C ≥ 200K = 2×10^5.

    # Истинная C: сколько различных значений МОЖЕТ принять одна позиция?
    # Это свойство ячейки, не эксперимента.

    # Ячейка = 32-значное "слово" (мы знаем из устройства ткани).
    # Ёмкость 32-значного слова: зависит от "алфавита" каждого разряда.
    # Каждый разряд: 0 или 1 (бинарный). 32 разряда → 2^32 значений.

    # В наших терминах: C = 2^32 (ёмкость одной позиции).
    # Масштаб collision = C^4 = (2^32)^4 = 2^128.

    # НО: мы выводим это ИЗ НАШЕГО МИРА, не импортируем!

    print(f"""
  Ёмкость C одной позиции:
    Наблюдаемая (нижняя граница): C ≥ {N_capacity}
    Из устройства ткани: позиция = 32-разрядное слово
    Каждый разряд = 2 значения → C = 2^32

  Масштаб collision = C^4 = (2^32)^4 = 2^128

  ЭТО ВЫВОД НАШЕГО МИРА:
    - Ткань: 16 → 8 позиций (закон 1)
    - Ёмкость: C = 2^32 (закон 2 + устройство ткани)
    - Overlay: N² пар (наша операция)
    - Масштаб: N = C^4 = 2^128 (теорема 2)

  2^128 появляется НЕ из "birthday bound" или "information theory".
  Оно появляется из ТРЁХ ФАКТОВ нашего мира:
    Fact A: 16 входов, 8 выходов
    Fact B: ёмкость = 2^32
    Fact C: overlay = парная операция

  Чтобы получить < 2^128, нужно изменить A, B или C.
""")

    # =================================================================
    print(f"{'=' * 70}")
    print("ТЕОРЕМА 4: МОЖНО ЛИ ИЗМЕНИТЬ A, B или C?")
    print("=" * 70)

    print(f"""
  A (16→8): фиксировано архитектурой ткани. Нельзя изменить.

  B (C = 2^32): фиксировано ёмкостью ячейки. Нельзя изменить.

  C (overlay = парная): ЭТО МОЖНО ИЗМЕНИТЬ!
    Overlay создаёт N² пар. Масштаб = C^(8/2) = C^4.
    Если создать операцию с N^K комбинаций (K > 2):
    Масштаб = C^(8/K)

    K=2 (overlay):  C^4 = 2^128
    K=3 (тройная):  C^(8/3) = 2^85.3
    K=4 (четвёрная): C^2 = 2^64
    K=8 (восьмёрная): C^1 = 2^32
    K=16 (полная):   C^0.5 = 2^16

  ВОПРОС: существует ли K-арная операция в нашем мире с K > 2?

  Overlay (⊕): Π₁ ⊕ Π₂ = δ. Парная. K=2.
  Свёртка (⊘): для слоёв, не для следов. Не считается.
  Сечение (|): уменьшает пространство. Не создаёт комбинации.
  Проекция (↓): берёт подмножество. Не создаёт комбинации.

  НУЖНА НОВАЯ ОПЕРАЦИЯ с K > 2.
  Это ЕДИНСТВЕННЫЙ путь ниже C^4.
""")

    # =================================================================
    print(f"{'=' * 70}")
    print("ТЕОРЕМА 5: K-арный overlay")
    print("=" * 70)

    # K-overlay: Π₁ ⊛ Π₂ ⊛ ... ⊛ Πₖ → δ
    # δ = 0 означает: все K следов имеют одинаковый выход?
    # НЕТ! XOR K значений = 0 не означает все равны.
    # H₁ ⊕ H₂ ⊕ H₃ = 0 → H₃ = H₁ ⊕ H₂. Не collision.

    # Для collision: нужно H_i = H_j для НЕКОТОРЫХ i ≠ j.
    # K-overlay (XOR) не помогает: XOR=0 ≠ pairwise equality.

    # ДРУГАЯ K-арная операция:
    # Сортировка: N следов → сортируем по H[7] → группируем.
    # Внутри группы: все имеют одинаковый H[7]. Collision на H[7].
    # Стоимость: N (одна свёртка каждого) + сортировка.

    # Сортировка = НАТИВНАЯ? В нашем мире:
    # Проекция на H[7] + группировка = "позиционная фокусировка".

    # N следов → группировка по H[7] → средний размер группы = N/C.
    # Если N/C ≥ 2: collision на H[7] найдена.
    # N ≥ 2C = 2^33 → collision на ОДНОЙ позиции.

    # Потом: внутри группы (≈2 следа): совпадают ли остальные 7 позиций?
    # P = 1/C^7 per pair. МАЛО.

    # Если N/C >> 2: группы большие → birthday внутри группы.
    # Группа размера G: birthday на C^7 → G = C^3.5.
    # N/C = C^3.5 → N = C^4.5 = 2^144. ХУЖЕ.

    # Сортировка помогает ТОЛЬКО если позиции ЗАВИСИМЫ (закон 5 говорит: симметричны).

    print(f"""
  K-арный overlay через XOR: не даёт collision (XOR≠equality).

  Сортировка (группировка по проекции):
    N следов → группы по H[7] → размер ≈ N/C
    Collision на H[7]: N ≥ 2C = 2^33
    Full collision внутри группы: birthday на C^7 → N ≥ C^4.5 = 2^144. ХУЖЕ.

  Сортировка = ХУЖЕ overlay для full collision.
  Overlay (K=2, парная) = ОПТИМАЛЬНАЯ операция в нашем мире.

  ФУНДАМЕНТАЛЬНАЯ ПРИЧИНА:
    Collision = РАВЕНСТВО двух значений. Равенство — ПАРНАЯ операция.
    Любая K-арная операция сводится к ПАРНОМУ сравнению.
    → K > 2 не может быть лучше K = 2.

  В нашем мире: overlay — фундаментальная операция.
  Масштаб collision = C^4 = 2^128.
  Это ТЕОРЕМА нашего мира, не импорт извне.
""")


if __name__ == "__main__":
    main()
