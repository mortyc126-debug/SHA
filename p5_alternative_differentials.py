def get_e3_vars(W0, W1):
    """Возвращает e3, f3, g3, h3, Dc3, Dh4 для normal и fault."""
    # normal
    tr_n = sha256_trace(W0, W1, rounds=5)
    tr_f = sha256_trace(W0+1, W1, rounds=5)
    # round-3 (0-indexed): state AT START of round 3 = after round 2
    e3_n = tr_n[3]['e']
    f3_n = tr_n[3]['f']
    g3_n = tr_n[3]['g']
    h3_n = tr_n[3]['h']
    c3_n = tr_n[3]['c']
    e3_f = tr_f[3]['e']
    h4_n = tr_n[4]['h']
