def rotr(x, n): return ((x >> n) | (x << (32 - n))) & MASK
def sig0(x): return rotr(x,7) ^ rotr(x,18) ^ (x>>3)
def sig1(x): return rotr(x,17) ^ rotr(x,19) ^ (x>>10)
def Sig0(x): return rotr(x,2) ^ rotr(x,13) ^ rotr(x,22)
def Sig1(x): return rotr(x,6) ^ rotr(x,11) ^ rotr(x,25)
def Ch(e,f,g): return (e&f)^(~e&g)&MASK
def Maj(a,b,c): return (a&b)^(a&c)^(b&c)

def schedule(W0, W1=0):
    """Полное расписание сообщений SHA-256 (64 слова)."""
    W = [0]*64
    W[0] = W0 & MASK
    W[1] = W1 & MASK
    for i in range(2, 16):
        W[i] = 0
    for i in range(16, 64):
        W[i] = (sig1(W[i-2]) + W[i-7] + sig0(W[i-15]) + W[i-16]) & MASK
    return W

def sha_step(a, b, c, d, e, f, g, h, w, k):
    """Один шаг SHA-256."""
    T1 = (h + Sig1(e) + Ch(e,f,g) + k + w) & MASK
    T2 = (Sig0(a) + Maj(a,b,c)) & MASK
    return T1, T2  # new_e = d+T1, new_a = T1+T2

def sha256_trace(W0, W1=0, rounds=8):
    """Трассировка SHA-256 (первые rounds шагов)."""
    W = schedule(W0, W1)
    a,b,c,d,e,f,g,h = H0
    trace = []
    for i in range(rounds):
        T1,T2 = sha_step(a,b,c,d,e,f,g,h, W[i], K[i])
        new_e = (d + T1) & MASK
        new_a = (T1 + T2) & MASK
        trace.append({'round':i,'a':a,'b':b,'c':c,'d':d,'e':e,'f':f,'g':g,'h':h,
                      'T1':T1,'T2':T2,'new_e':new_e,'new_a':new_a,'W':W[i]})
        a,b,c,d,e,f,g,h = new_a,a,b,c,new_e,e,f,g
    return trace

def compute_De_n(W0, dW0, n_rounds=8):
    """Вычисляет De_i для i=0..n_rounds-1 при ΔW0=dW0."""
    t_normal = sha256_trace(W0, rounds=n_rounds)
    t_fault  = sha256_trace(W0 + dW0, rounds=n_rounds)
    De = []
    for i in range(n_rounds):
        de = (t_fault[i]['e'] - t_normal[i]['e']) & MASK
        De.append(de)
    return De

