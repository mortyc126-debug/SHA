def mitm_truncated_hash():
    print("=" * 68)
    print("4. MitM ДЛЯ УСЕЧЁННОГО ХЭША С КАСКАДОМ")
    print("=" * 68)
    print()
    print("  УСЕЧЁННЫЙ ХЭШ = только первые k бит выходного хэша")
    print()
    print("  Наша атака на k-битный усечённый хэш:")
    print("  ─────────────────────────────────────────────────────")
    print()
    print("  Метод:")
    print("  1. Зафиксировать структуру M_normal через W0, W1 (с De3=0)")
    print("  2. W11-W15 = свободные 5×32 = 160 бит")
    print("  3. Для каждого W11-W15 (5 слов): хэш меняется случайно")
    print("  4. Ищем два выбора (W11..W15) и (W11'..W15') с совпадением")
    print("     в первых k битах хэша")
    print()
    print("  Но: M_normal и M'_normal = разные сообщения!")
    print("  (Если выбрать пары из разных (W0,W1), получим коллизию)")
    print()

    # Демонстрация: birthday collision на 32-битном усечённом хэше
    print("  ДЕМОНСТРАЦИЯ: birthday collision на 32-битном усечении e-регистра")
    print("  (Используем только e-регистр выходного состояния)")
    print()

    W0_BASE = 0xc5bde324
    W1_SAT  = 0x3d7bd9d5

    # Собираем множество хэшей варьируя W15
    hash_table = {}
    TRIALS = 2**16

    print(f"  Генерируем {TRIALS} = 2^16 хэшей (варьируя W15)...")

    collision_found = None
    for i in range(TRIALS):
        W15 = i
        W_n = [W0_BASE, W1_SAT] + [0]*13 + [W15]
        state = tuple(H0)
        W_full = make_schedule(W_n)
        for r in range(64):
            state = sha_step(state, W_full[r], K[r])
        # Финальный хэш (только e-компонента, первые 32 бита)
        h_e = (H0[4] + state[4]) & MASK

        if h_e in hash_table:
            j = hash_table[h_e]
            collision_found = (i, j, h_e)
            break
        hash_table[h_e] = i

    if collision_found:
        i, j, h_e = collision_found
        W_i = [W0_BASE, W1_SAT] + [0]*13 + [i]
        W_j = [W0_BASE, W1_SAT] + [0]*13 + [j]
        h_i = sha256_compress(W_i)
        h_j = sha256_compress(W_j)
        print(f"  КОЛЛИЗИЯ НАЙДЕНА за {i+1} попыток (ожидалось ~2^16):")
        print(f"    W15_i = {i:#010x}")
        print(f"    W15_j = {j:#010x}")
        print(f"    Хэш-e = {h_e:#010x}  (совпадает!)")
        print(f"    Полный хэш[i]: {' '.join(f'{x:#010x}' for x in h_i)}")
        print(f"    Полный хэш[j]: {' '.join(f'{x:#010x}' for x in h_j)}")
        match_full = (h_i == h_j)
        print(f"    Полная коллизия (256 бит): {'ДА' if match_full else 'нет (только e-компонента)'}")
    else:
        print(f"  Коллизия не найдена за {TRIALS} попыток (нужно больше).")
    print()


