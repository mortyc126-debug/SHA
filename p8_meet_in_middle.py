def rotr(x, n): return ((x >> n) | (x << (32 - n))) & MASK
def sig0(x): return rotr(x,7) ^ rotr(x,18) ^ (x>>3)
def sig1(x): return rotr(x,17) ^ rotr(x,19) ^ (x>>10)
def Sig0(x): return rotr(x,2) ^ rotr(x,13) ^ rotr(x,22)
def Sig1(x): return rotr(x,6) ^ rotr(x,11) ^ rotr(x,25)
def Ch(e,f,g):  return (e & f) ^ (~e & g) & MASK
def Maj(a,b,c): return (a & b) ^ (a & c) ^ (b & c)

def sha_step(state, W_i, K_i):
    a, b, c, d, e, f, g, h = state
    T1 = (h + Sig1(e) + Ch(e, f, g) + K_i + W_i) & MASK
    T2 = (Sig0(a) + Maj(a, b, c)) & MASK
    return (T1 + T2) & MASK, a, b, c, (d + T1) & MASK, e, f, g

def make_schedule(W16_list):
    W = list(W16_list)[:16] + [0]*48
    W += [0]*(16 - len(W16_list)) if len(W16_list) < 16 else []
    W = W[:16] + [0]*48
    for i in range(16, 64):
        W[i] = (sig1(W[i-2]) + W[i-7] + sig0(W[i-15]) + W[i-16]) & MASK
    return W

def run_rounds(state, W_full, start, end):
    """Прогнать раунды [start, end) используя полный schedule W_full."""
    for i in range(start, end):
        state = sha_step(state, W_full[i], K[i])
    return state

def sha256_compress(W16_list):
    """Полная компрессия SHA-256."""
    W = make_schedule(W16_list)
    state = tuple(H0)
    for i in range(64):
        state = sha_step(state, W[i], K[i])
    return tuple((H0[j] + state[j]) & MASK for j in range(8))


