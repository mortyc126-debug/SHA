def avalanche_analysis():
    print("=" * 68)
    print("6. BONUS: КАСКАД vs ЛАВИННЫЙ КРИТЕРИЙ SHA-256")
    print("=" * 68)
    print()

    W0_BASE = 0xc5bde324
    W1_SAT  = 0x3d7bd9d5

    def hamming(a, b):
        return bin((a ^ b) & MASK).count('1')

    # Без каскада: 1-битное изменение W0
    W_normal = [W0_BASE, W1_SAT] + [0]*14
    W_flip1  = [(W0_BASE + 1) & MASK, W1_SAT] + [0]*14

    # С каскадом (из П-7): добавляем ΔW3-ΔW10
    cascade_DWs = [0, 0, 0,
                   0x1c1ff87f,  # ΔW3
                   0xe360037f,  # ΔW4
                   0x0037c7f4fc & MASK,  # ΔW5
                   0xe319dbcf,  # ΔW6
                   0x3090822e,  # ΔW7
                   0x9a6d9669,  # ΔW8
                   0xe85e36c4,  # ΔW9
                   0xd93f249b,  # ΔW10
                   0, 0, 0, 0, 0]
    W_cascade = [(W_normal[i] + cascade_DWs[i]) & MASK for i in range(16)]

    print(f"  Лавинный критерий: сколько бит выхода изменяется при 1-битном входе?")
    print(f"  Ожидаемое (хорошая хэш-ф.): ~128 из 256 бит изменяется")
    print()

    def count_diff_bits(W_a, W_b):
        h_a = sha256_compress(W_a)
        h_b = sha256_compress(W_b)
        total = sum(hamming(h_a[i], h_b[i]) for i in range(8))
        return total, h_a, h_b

    bits_1bit, h_n, h_1 = count_diff_bits(W_normal, W_flip1)
    bits_cascade, h_n2, h_c = count_diff_bits(W_normal, W_cascade)

    print(f"  1-битное изменение W0:")
    print(f"    Изменилось битов в выходе: {bits_1bit} из 256")
    print()
    print(f"  Каскадное изменение (ΔW0=1 + ΔW3..ΔW10):")
    print(f"    Изменилось битов в выходе: {bits_cascade} из 256")
    print()
    print(f"  Оба случая: выходной хэш изменился значительно → SHA-256 сохраняет")
    print(f"  лавинный критерий даже при каскадном входе.")
    print()
    print(f"  Это подтверждает: каскад De3..De11=0 не нарушает диффузионные")
    print(f"  свойства SHA-256 — полный хэш всё равно различается сильно.")
    print()


