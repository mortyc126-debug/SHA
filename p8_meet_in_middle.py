def demo_11round_collision():
    print("=" * 68)
    print("3. ДЕМОНСТРАЦИЯ: 11-РАУНДОВАЯ e-КОЛЛИЗИЯ (De3..De11=0)")
    print("=" * 68)
    print()
    print("  Алгоритм:")
    print("  1. Выбрать случайный W0")
    print("  2. Sweep W1 до De3=0 (ожидаемо ~2^22 попыток)")
    print("  3. Применить каскад ΔW3..ΔW10 детерминированно")
    print("  4. Получить пару с De3..De11=0 за ~2^22 операций")
    print()

    W0_BASE = 0xc5bde324
    W1_SAT  = 0x3d7bd9d5

    # Каскадные ΔW из П-7 (для W0=W0_BASE, W1=W1_SAT, ΔW0=1):
    # De3=De4=...=De11=0 достигнуто этими значениями
    W0 = W0_BASE
    W1 = W1_SAT
    DWs = [0]*16
    DWs[0] = 1            # ΔW0 = 1
    # ΔW3..ΔW10 из П-7 раздел 3:
    cascade_values = [
        0x1c1ff87f,  # ΔW3 → De4=0
        0xe360037f,  # ΔW4 → De5=0
        0x37c7f4fc,  # ΔW5 → De6=0
        0xe319dbcf,  # ΔW6 → De7=0
        0x3090822e,  # ΔW7 → De8=0
        0x9a6d9669,  # ΔW8 → De9=0
        0xe85e36c4,  # ΔW9 → De10=0
        0xd93f249b,  # ΔW10 → De11=0
    ]
    for idx, val in enumerate(cascade_values):
        DWs[3 + idx] = val

    print(f"  Используем W0={W0:#010x}, W1={W1:#010x}")
    print(f"  Каскадные ΔW:")
    for i, dw in enumerate(DWs):
        if dw != 0:
            print(f"    ΔW[{i}] = {dw:#010x}")
    print()

    # Строим оба сообщения
    W_n = [W0 if i==0 else (W1 if i==1 else 0) for i in range(16)]
    W_f = [(W_n[i] + DWs[i]) & MASK for i in range(16)]

    W_n_full = make_schedule(W_n)
    W_f_full = make_schedule(W_f)

    # Трассируем 12 раундов
    state_n = tuple(H0)
    state_f = tuple(H0)
    zeros_e = []
    print(f"  {'Раунд':6s}  {'e_normal':10s}  {'e_faulty':10s}  {'De_e':10s}  {'статус'}")
    print("  " + "-" * 60)
    for r in range(12):
        state_n = sha_step(state_n, W_n_full[r], K[r])
        state_f = sha_step(state_f, W_f_full[r], K[r])
        De = (state_f[4] - state_n[4]) & MASK
        status = "De_e=0 !" if De == 0 else ""
        if De == 0: zeros_e.append(r+1)
        print(f"  {r+1:6d}  {state_n[4]:#010x}  {state_f[4]:#010x}  {De:#010x}  {status}")

    print()
    print(f"  Раунды с De_e=0: {zeros_e}")
    print(f"  Длина цепочки нулей: {len(zeros_e)} раундов")
    print()

    # Верификация: e_normal = e_faulty в нулевых раундах
    all_match = all(
        sha_step(
            run_rounds(tuple(H0), W_n_full, 0, r),
            W_n_full[r], K[r]
        )[4] ==
        sha_step(
            run_rounds(tuple(H0), W_f_full, 0, r),
            W_f_full[r], K[r]
        )[4]
        for r in [z-1 for z in zeros_e]
    )

    print(f"  Частичная коллизия (e-регистр, 11 раундов): {'ПОДТВЕРЖДЕНА' if len(zeros_e)>=9 else 'частично'}")
    print(f"  Стоимость: ~2^22 операций (выбор W1 из П-3)")
    print()
    return zeros_e


